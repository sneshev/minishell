#include "ft_printf.h"

void	print_nbr(int n, int *cnt)
{
	char	c;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*cnt += 11;
	}
	else
	{
		if (n < 0)
		{
			n *= -1;
			write(1, "-", 1);
			*cnt += 1;
		}
		if (n > 9)
			print_nbr(n / 10, cnt);
		c = (n % 10) + '0';
		write (1, &c, 1);
		*cnt += 1;
	}
}
