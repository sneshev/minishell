#include "ft_printf.h"

void	put_u(unsigned int n, int *cnt)
{
	char	c;

	if (n > 9)
		put_u(n / 10, cnt);
	c = (n % 10) + '0';
	write (1, &c, 1);
	*cnt += 1;
}
